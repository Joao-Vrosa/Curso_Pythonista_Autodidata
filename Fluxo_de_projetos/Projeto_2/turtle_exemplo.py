from turtle import Turtle

# Iniciar uma Turtle
t = Turtle()
# Definir a velocidade
t.speed(1)
# Movimentar a turtle para frente
t.forward(100)
# Rotacionar em X graus para a direita
t.right(90)
t.forward(100)
# Rotacionar em X graus para a esquerda
t.left(90)
t.forward(100)
# Movimentar a turtle para tras
t.backward(200)
input()
